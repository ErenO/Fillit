#include "header.h"

int		ft_check_plus(char *av, int i)
{
	int k;
	int j;
	int n;
	int h;

	k = 0;
	j = 4;
	n = 0;
	h = 0;
	while (av[i] != '\0' && j > 0)
	{
		if (av[i] == '#')
			h++;
		if (av[i] == '#' && av[i + 1] == '#')
			k++;
		if (av[i] == '#' && av[i + 5] == '#' && n < 16)
			k++;
		if (av[i] == '\n')
			j--;
		i++;
		n++;
	}
	if ((h != 4 && n > 0) || k < 3)
		i = 0;
	return (i);
}
