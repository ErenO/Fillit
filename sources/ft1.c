#include "header.h"

int	*ft1(int *position, int *ijk)
{
	position[ijk[1]] = ijk[0] - ijk[2] + 1;
	position[ijk[1] + 1] = 1;
	ijk[1] = ijk[1] + 2;
	ijk[0] = ijk[0] + 1;
	return (position);
}
