#include "header.h"

void	ft_make_grid(char **tab, int size_map)
{
	int i;
	int j;

	i = 0;
	while (i < size_map)
	{
		j = 0;
		while (j < size_map)
		{
			tab[i][j] = '.';
			j++;
		}
		i++;
	}
}
